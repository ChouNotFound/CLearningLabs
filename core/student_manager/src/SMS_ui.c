/* 显示菜单 */
void sms_ui_show_menu(void)
{
    printf("\n---------- 菜单 ----------\n");
    printf("1. 添加学生信息\n");
    printf("2. 显示所有学生信息\n");
    printf("3. 查找学生信息\n");
    printf("4. 删除学生信息\n");
    printf("5. 返回主菜单\n");
    printf("6. 退出系统\n");
    printf("-------------------------\n");
}

/* 获取用户选择 */
/* @return 用户选择的菜单项 */
int sms_ui_get_user_choice(void)
{
    int choice;
    printf("请选择操作 (1-6): ");
    scanf("%d", &choice);
    return choice;
}