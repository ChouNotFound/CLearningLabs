/* 显示棋盘 */
void ttt_display_board(ttt_board_t board)
{
    printf("\n    1   2   3\n");
    printf("  -------------\n");
    for (int i = 0; i < BOARD_SIZE; i++)
    {
        printf("%d |", i + 1);
        for (int j = 0; j < BOARD_SIZE; j++)
        {
            printf(" %c |", board[i][j]);
        }
        printf("\n  -------------\n");
    }
    printf("\n");
}